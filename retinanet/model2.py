                scores = torch.squeeze(classification[:, :, i])
                #print('scoressssss',scores)
                scores_over_thresh = (scores > 0.05)
                if scores_over_thresh.sum() == 0:
                    # no boxes to NMS, just continue
                    continue

                scores = scores[scores_over_thresh]
                anchorBoxes = torch.squeeze(transformed_anchors)
                anchorBoxes = anchorBoxes[scores_over_thresh]
                #print('nm22222222222222',anchorBoxes)
                #box_scores = format_prediction_string(anchorBoxes,scores)
                #box_scores = np.array(box_scores)
                #box_scores = torch.from_numpy(box_scores)
                #print('nms:11111111111111111111111111111111',box_scores)
                anchors_nms_idx = nms(anchorBoxes,scores,0.5)
                #anchors_nms_idx = soft_nms(box_scores, 0.8)
                #print('33333333333333333331115551',anchors_nms_idx)
                #finalResult[0].extend(anchors_nms_idx[:,-1])
                finalResult[0].extend(scores[anchors_nms_idx])
                #print('2222222222222222',finalResult[0])
                
                finalResult[1].extend(torch.tensor([i] * anchors_nms_idx.shape[0]))
                #print('333333333333333333',finalResult[1])
                finalResult[2].extend(anchorBoxes[anchors_nms_idx])
                #finalResult[2].extend(anchors_nms_idx[:,0:3])
                #print('444444444444444444444',[i])
                #finalScores = torch.cat((finalScores, anchors_nms_idx[:,-1].float()))
                finalScores = torch.cat((finalScores, scores[anchors_nms_idx]))
                #print('5555555555555',anchors_nms_idx.shape[0])
